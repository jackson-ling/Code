// 本质就是直角三角形的打印，只需要控制每一行的个数都相同就行
// 这里以右下直角三角形的朝向举例子，其余朝向大同小异

#include <stdio.h>
int main()
{
    int n;
    scanf("%d", &n); // 控制平行四边形的行数
    for (int i = 1; i <= n; i++)
    {
        for (int k = n - i; k >= 1; k--)
        {
            printf(" ");
        }
        for (int j = 1; j <= 5; j++) // 控制平行四边形的边长
        {
            printf("*");
        }
        printf("\n");
    }
}